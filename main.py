import xml.etree.ElementTree as ET
import cv2
import numpy as np
import os

def create_masks_from_xml(xml_file_path, image_dir, output_mask_dir):
    """
    Parses CVAT annotations from an XML file and generates binary masks.
    """
    if not os.path.exists(output_mask_dir):
        os.makedirs(output_mask_dir)

    try:
        tree = ET.parse(xml_file_path)
        root = tree.getroot()

        for image_element in root.findall('image'):
            image_name = image_element.get('name')
            image_width = int(image_element.get('width'))
            image_height = int(image_element.get('height'))

            mask = np.zeros((image_height, image_width), dtype=np.uint8)
            polygon_element = image_element.find('polygon')

            if polygon_element is not None:
                points_string = polygon_element.get('points')
                coord_pairs = points_string.split(';')

                coordinates = []
                for pair in coord_pairs:
                    x, y = map(float, pair.split(','))
                    coordinates.append((int(x), int(y)))

                points_array = np.array(coordinates, dtype=np.int32)
                points_array = points_array.reshape((-1, 1, 2))
                cv2.fillPoly(mask, [points_array], 255)

            mask_save_path = os.path.join(output_mask_dir, image_name)
            cv2.imwrite(mask_save_path, mask)

    except FileNotFoundError:
        print(f"Error: The file '{xml_file_path}' was not found.")
    except ET.ParseError:
        print(f"Error: Failed to parse the XML file. Check its format.")

